# Token-replacement stubs (NOT str.format — the Vue/TS/JSON bodies are
# brace-heavy). Placeholders: __NAME__ / __LAYER__ / __SNAKE__.
_VUE_STUB = """\
<script setup lang="ts">
// Layer-__LAYER__ component generated by Forge (component___NAME__).
defineProps<{
  label: string
}>()
</script>

<template>
  <div class="__SNAKE__">{{ label }}</div>
</template>
"""

_FEATURE_TOML = """\
[feature]
name = "__NAME__"
version = "0.1.0"
summary = "__NAME__ — Layer-__LAYER__ component (scaffolded)."
category = "component"
stability = "experimental"
layer = __LAYER__
"""

_INIT_PY = '''\
"""__NAME__ — a Layer-__LAYER__ component. The loader auto-registers its emitter
fragment from this manifest; no options/fragments of its own."""

from __future__ import annotations

from forge.api import ForgeAPI


def register(api: ForgeAPI) -> None:  # noqa: ARG001
    return None
'''

_PROPS_STUB = """\
{
  "$schema": "http://json-schema.org/draft-07/schema#",
  "title": "__NAME__Props",
  "type": "object",
  "properties": { "label": { "type": "string" } },
  "required": ["label"],
  "additionalProperties": false
}
"""


def _render(stub: str, *, name: str, layer: int, snake: str) -> str:
    return (
        stub.replace("__NAME__", name).replace("__LAYER__", str(layer)).replace("__SNAKE__", snake)
    )


def _snake(name: str) -> str:
    s = re.sub(r"(?<!^)(?=[A-Z])", "_", name).lower()
    return re.sub(r"[^a-z0-9_]", "_", s)


def _scaffold_component(name: str, *, layer: int = 1, root: Path | None = None) -> None:
    """Render a layer-aware component skeleton under ``root`` (default features dir)."""
    if not _PASCAL.match(name):
        print(f"Component name must be PascalCase (got {name!r}).", file=sys.stderr)
        sys.exit(2)
    if layer not in (1, 2, 3):
        print(f"layer must be 1, 2, or 3 (got {layer}).", file=sys.stderr)
        sys.exit(2)
    if root is None:
        root = Path(__file__).resolve().parents[2] / "features"

    snake = _snake(name)
    feat = root / snake
    if feat.exists():
        print(f"Refusing to overwrite existing directory: {feat}", file=sys.stderr)
        sys.exit(1)

    emitter = (
        feat / "templates" / f"component_{name}" / "all" / "files" / "src" / "shared" / "components"
    )
    emitter.mkdir(parents=True)
    (feat / "feature.toml").write_text(_render(_FEATURE_TOML, name=name, layer=layer, snake=snake))
    (feat / "__init__.py").write_text(_render(_INIT_PY, name=name, layer=layer, snake=snake))
    (feat / f"{name}.props.schema.json").write_text(
        _render(_PROPS_STUB, name=name, layer=layer, snake=snake)
    )
    (emitter / f"{name}.vue").write_text(_render(_VUE_STUB, name=name, layer=layer, snake=snake))

    print(f"Scaffolded Layer-{layer} component {name!r} at {feat}")
    for p in sorted(feat.rglob("*")):
        if p.is_file():
            print(f"  + {p.relative_to(root)}")
