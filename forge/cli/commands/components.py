"""`forge --component-cmd ...` / `--template-cmd ...` — layered-component verbs.

Subcommands:

* ``--component-cmd list``      — enumerate registered components (all layers).
* ``--component-cmd scaffold``  — render a layer-aware component skeleton.
* ``--template-cmd list``       — enumerate Layer-3 templates (layer == 3).

Component *selection* into a project is config-driven (``components: [...]``);
these verbs are for discovery + authoring, mirroring ``--features-cmd``.
"""

from __future__ import annotations

import json
import re
import sys
from pathlib import Path
from typing import cast

from forge import telemetry

_PASCAL = re.compile(r"^[A-Z][A-Za-z0-9]*$")


def _ensure_loaded() -> None:
    """Populate COMPONENT_REGISTRY from built-in features if not already."""
    from forge.feature_loader import load_builtin_features  # noqa: PLC0415

    load_builtin_features()


def _component_rows(*, layer: int | None = None) -> list[dict[str, object]]:
    from forge.components import COMPONENT_REGISTRY  # noqa: PLC0415

    rows: list[dict[str, object]] = []
    for node in sorted(COMPONENT_REGISTRY.values(), key=lambda n: n.name):
        if layer is not None and node.layer != layer:
            continue
        rows.append(
            {
                "name": node.name,
                "layer": node.layer,
                "version": node.version,
                "contract": node.contract,
                "children": sorted(node.children),
            }
        )
    return rows


def _print_rows(rows: list[dict[str, object]], *, json_output: bool, empty_msg: str) -> None:
    if json_output:
        print(json.dumps(rows, indent=2))
        return
    if not rows:
        print(empty_msg)
        return
    width = max(len(str(r["name"])) for r in rows)
    for r in rows:
        child_names = cast("list[str]", r["children"])
        children = f"  children: {', '.join(child_names)}" if child_names else ""
        contract = f"  contract: {r['contract']}" if r["contract"] else ""
        print(f"  L{r['layer']}  {str(r['name']):<{width}}  {r['version']}{contract}{children}")


def _dispatch_components(
    subcommand: str,
    *,
    json_output: bool = False,
    name: str | None = None,
    layer: int = 1,
) -> None:
    """Dispatch a ``--component-cmd`` subcommand and exit."""
    if subcommand == "list":
        _ensure_loaded()
        _print_rows(_component_rows(), json_output=json_output, empty_msg="No components.")
        telemetry.emit(telemetry.EVENT_COMPONENT_RAN, action="list")
        sys.exit(0)

    if subcommand == "scaffold":
        if not name:
            print(
                "scaffold requires a name: "
                "`forge --component-cmd scaffold --component-name <PascalName>`",
                file=sys.stderr,
            )
            sys.exit(2)
        _scaffold_component(name, layer=layer)
        telemetry.emit(telemetry.EVENT_COMPONENT_RAN, action="scaffold")
        sys.exit(0)

    print(f"Unknown component subcommand: {subcommand!r}", file=sys.stderr)
    sys.exit(2)


def _dispatch_templates(
    subcommand: str, *, json_output: bool = False, name: str | None = None
) -> None:
    """Dispatch a ``--template-cmd`` subcommand and exit."""
    if subcommand == "list":
        _ensure_loaded()
        _print_rows(_component_rows(layer=3), json_output=json_output, empty_msg="No templates.")
        telemetry.emit(telemetry.EVENT_TEMPLATE_RAN, action="list")
        sys.exit(0)

    print(f"Unknown template subcommand: {subcommand!r}", file=sys.stderr)
    sys.exit(2)


