class TestRegeneratePackages:
    """The CLI entry point writes the canvas-package event-union files."""

    def test_writes_every_target(self, tmp_path: Path) -> None:
        written = regenerate_packages(repo_root=tmp_path)
        rels = {p.relative_to(tmp_path).as_posix() for p in written}
        assert rels == {
            "packages/canvas-vue/src/generated/events.ts",
            "packages/canvas-svelte/src/generated/events.ts",
            "packages/forge-canvas-dart/lib/src/generated/events.dart",
        }

    def test_outputs_are_byte_stable(self, tmp_path: Path) -> None:
        regenerate_packages(repo_root=tmp_path)
        snapshot = {
            p: (tmp_path / p).read_bytes()
            for p in (
                "packages/canvas-vue/src/generated/events.ts",
                "packages/canvas-svelte/src/generated/events.ts",
                "packages/forge-canvas-dart/lib/src/generated/events.dart",
            )
        }
        regenerate_packages(repo_root=tmp_path)
        for rel, expected in snapshot.items():
            assert (tmp_path / rel).read_bytes() == expected, f"{rel} changed across runs"

    def test_vue_and_svelte_emit_same_typescript(self, tmp_path: Path) -> None:
        # Same single-source invariant as canvas-props: vue and svelte
        # must agree byte-for-byte on the TS event-union module.
        regenerate_packages(repo_root=tmp_path)
        vue = (tmp_path / "packages/canvas-vue/src/generated/events.ts").read_text()
        svelte = (tmp_path / "packages/canvas-svelte/src/generated/events.ts").read_text()
        assert vue == svelte


class TestExhaustiveCoverage:
    """The union must cover every shipped schema — drift detection."""

    def test_kinds_list_matches_loaded_schemas(self) -> None:
        schemas = load_event_schemas()
        kinds = _kinds_list(schemas)
        assert {title for title, _ in kinds} == _EXPECTED_TITLES

    def test_every_loaded_schema_appears_in_typescript_union(self) -> None:
        schemas = load_event_schemas()
        out = emit_typescript(schemas)
        for s in schemas:
            kind = _kind_for(s)
            # The kind slug AND the title must both be referenced — the
            # type alias declaration is the load-bearing line.
            assert f'kind: "{kind}"' in out, f"kind {kind} missing from union"
            assert f"{s.title}WithKind" in out, f"variant {s.title}WithKind missing"

    def test_every_loaded_schema_appears_in_dart_sealed(self) -> None:
        schemas = load_event_schemas()
        out = emit_dart(schemas)
        for s in schemas:
            assert f"class {s.title}Event extends AgUiEvent" in out

    def test_every_loaded_schema_appears_in_pydantic_union(self) -> None:
        schemas = load_event_schemas()
        out = emit_pydantic(schemas)
        for s in schemas:
            kind = _kind_for(s)
            assert f'Literal["{kind}"]' in out


